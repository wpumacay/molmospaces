def test_dynamics_randomization(
    config: RandomizationTestConfig, scene_path: str, baseline_file: Path
):
    """Test that dynamics randomization works correctly."""
    if not config.enable_dynamics_randomization:
        pytest.skip("Dynamics randomization is disabled in config")

    # Load baseline data
    print(f"Loading baseline data from: {baseline_file}")
    baseline_data = load_baseline_data(baseline_file)

    # Setup model and randomizers
    model, data, randomizers = _setup_model_and_randomizers(scene_path, config)
    dynamics_randomizer = randomizers.get("dynamics")

    if dynamics_randomizer is None:
        pytest.skip("Dynamics randomizer not initialized")

    # Apply dynamics randomization
    print("\nApplying dynamics randomization...")
    objects = get_all_bodies_with_joints_as_mlspaces_objects(model, data)
    if objects:
        dynamics_randomizer.randomize_objects(objects)
        print(f"  ✓ Dynamics randomized for {len(objects)} objects")
    else:
        pytest.skip("No objects found for dynamics randomization")

    mujoco.mj_forward(model, data)

    # Check results
    print("\nChecking dynamics randomization...")
    dynamics_results = check_dynamics_randomization(
        model, data, baseline_data["dynamics"], dynamics_randomizer
    )

    num_randomized = sum(
        1 for v in dynamics_results["randomized"].values() if v.get("status") == "OK - randomized"
    )
    print(f"  Randomized: {num_randomized}/{len(dynamics_results['randomized'])} objects")
    print(f"  Errors: {len(dynamics_results['errors'])}")

    # Assertions
    assert len(dynamics_results["errors"]) == 0, (
        f"Found {len(dynamics_results['errors'])} errors in dynamics randomization"
    )
    assert num_randomized > 0, "No objects were randomized for dynamics"


def test_texture_randomization(
    config: RandomizationTestConfig, scene_path: str, baseline_file: Path
):
    """Test that texture/material and camera image randomization works correctly."""
    if not config.enable_texture_randomization:
        pytest.skip("Texture randomization is disabled in config")

    # Load baseline data
    print(f"Loading baseline data from: {baseline_file}")
    baseline_data = load_baseline_data(baseline_file)

    # Setup model and randomizers
    model, data, randomizers = _setup_model_and_randomizers(scene_path, config)
    texture_randomizer = randomizers.get("texture")

    if texture_randomizer is None:
        pytest.skip("Texture randomizer not initialized")

    # Apply texture randomization
    print("\nApplying texture randomization...")
    texture_randomizer.randomize_by_category(data)
    print("  ✓ Textures randomized")

    mujoco.mj_forward(model, data)

    # Check texture/material results
    print("\nChecking texture/material randomization...")
    texture_results = check_texture_material_randomization(model, baseline_data["texture_material"])
    print(f"  Randomized: {len(texture_results['randomized'])} geoms")
    print(f"  Errors: {len(texture_results['errors'])}")

    # Check camera images
    print("\nChecking camera images...")
    camera_results = check_camera_image_randomization(
        model, data, baseline_data["camera_images"], save_path=OUTPUT_DATA_DIR
    )
    num_changed = sum(1 for v in camera_results["images"].values() if v.get("changed", False))
    print(f"  Changed: {num_changed}/{len(camera_results['images'])} images")
    print(f"  Errors/Warnings: {len(camera_results['errors'])}")
    print(f"  Saved randomized images to: {OUTPUT_DATA_DIR}")

    # Compare visual similarity of baseline and randomized images
    print("\nComparing visual similarity of baseline and randomized images...")
    for cam_name, cam_data in camera_results["images"].items():
        baseline_image_path = cam_data.get("baseline_image_path")
        randomized_image_path = cam_data.get("current_image_path")

        if baseline_image_path and randomized_image_path:
            similarity_results = compare_image_visual_similarity(
                Path(baseline_image_path),
                Path(randomized_image_path),
                min_ssim=0.0,  # Images should differ (randomization occurred)
                max_ssim=0.95,  # Allow some similarity but not too much
            )

            if similarity_results["errors"]:
                print(f"  {cam_name}: Errors - {similarity_results['errors']}")
            else:
                ssim_score = similarity_results["ssim_score"]
                print(
                    f"  {cam_name}: SSIM={ssim_score:.4f}, "
                    f"mean_diff={similarity_results['mean_pixel_diff']:.2f}, "
                    f"max_diff={similarity_results['max_pixel_diff']:.2f}, "
                    f"diff_pixels={similarity_results['percent_different_pixels']:.2f}%"
                )

                # Images should be different (low SSIM) if randomization worked
                # SSIM < 0.95 indicates meaningful visual difference
                if ssim_score >= 0.95:
                    camera_results["errors"].append(
                        f"WARNING: {cam_name} images are too similar (SSIM={ssim_score:.4f}), "
                        f"randomization may not have affected visual appearance"
                    )

    # Assertions
    assert len(texture_results["errors"]) == 0, (
        f"Found {len(texture_results['errors'])} errors in texture/material randomization"
    )
    assert len(texture_results["randomized"]) > 0, "No geoms were randomized for texture/material"
    assert len(camera_results["errors"]) == 0, (
        f"Found {len(camera_results['errors'])} errors in camera image randomization"
    )
    assert num_changed > 0, "Camera images did not change after texture randomization"


def test_lighting_randomization(
    config: RandomizationTestConfig, scene_path: str, baseline_file: Path
):
    """Test that lighting randomization works correctly."""
    if not config.enable_lighting_randomization:
        pytest.skip("Lighting randomization is disabled in config")

    # Load baseline data
    print(f"Loading baseline data from: {baseline_file}")
    baseline_data = load_baseline_data(baseline_file)

    # Setup model and randomizers
    model, data, randomizers = _setup_model_and_randomizers(scene_path, config)
    lighting_randomizer = randomizers.get("lighting")

    if lighting_randomizer is None:
        pytest.skip("Lighting randomizer not initialized")

    # Apply lighting randomization
    print("\nApplying lighting randomization...")
    lighting_randomizer.randomize(data)
    print("  ✓ Lighting randomized")

    mujoco.mj_forward(model, data)

    # Check results
    print("\nChecking lighting randomization reproducibility...")
    lighting_results = check_lighting_randomization(model, baseline_data["lighting"])
    num_matches = sum(1 for v in lighting_results["randomized"].values() if v.get("matches", False))
    num_total = len(lighting_results["randomized"])
    print(f"  Matches baseline: {num_matches}/{num_total} lights")
    print(f"  Errors: {len(lighting_results['errors'])}")

    # Assertions
    # All lights should match the baseline (same seed should produce same randomized values)
    assert len(lighting_results["errors"]) == 0, (
        f"Found {len(lighting_results['errors'])} errors in lighting randomization reproducibility:\n"
        + "\n".join(f"  - {err}" for err in lighting_results["errors"][:10])
        + (
            f"\n  ... and {len(lighting_results['errors']) - 10} more errors"
            if len(lighting_results["errors"]) > 10
            else ""
        )
    )
    assert num_matches == num_total, (
        f"Only {num_matches}/{num_total} lights matched the baseline (expected all to match with same seed)"
    )
